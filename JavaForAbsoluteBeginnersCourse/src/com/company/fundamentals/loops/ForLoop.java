package com.company.fundamentals.loops;

public class ForLoop {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println("i");

        }

        //reverse

        for (int j = 1250; j >= 480; j--){
            System.out.println(j);
        }
    }
}
